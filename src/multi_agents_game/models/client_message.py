from enum import IntEnum, unique
from typing import Dict, final, Any
from pydantic import BaseModel


################################################################################################################
################################################################################################################
################################################################################################################
@final
@unique
class ClientMessageHead(IntEnum):
    NONE = 0
    AGENT_EVENT = 1


################################################################################################################
################################################################################################################
################################################################################################################
@final
class ClientMessage(BaseModel):
    head: int = ClientMessageHead.NONE
    body: Dict[str, Any] = {}


