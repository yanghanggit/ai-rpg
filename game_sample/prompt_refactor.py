## 游戏流程
"""
1. 游戏启动: 你将获得初始设定，包括角色、场景和道具信息，以及游戏剧情的开端设定。
2. 制定计划，其中包含你将要执行的动作。
3. 执行动作后，系统会根据你的行为触发事件或提供提示。
4. 根据第3步的结果进行思考，并返回第2步。
5. 重复第2至第4步，直至游戏结束。
### 关键名词
- 游戏启动
- 计划
- 动作
- 事件与提示
"""
