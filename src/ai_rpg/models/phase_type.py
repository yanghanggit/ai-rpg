"""战斗阶段/流程枚举模型定义
PhaseType 描述战斗流程中各个阶段，供 StatusEffect、系统及 LLM 提示词复用。
"""

from enum import StrEnum, unique
from typing import final


###############################################################################################################################################
@final
@unique
class PhaseType(StrEnum):
    """战斗阶段"""

    DRAW = "draw"
    ARBITRATION = "arbitration"
    ROUND_END = "round_end"


