## 游戏规则
"""
### 核心要素：
角色：包括人、动物、怪物等与之交流和交互的对象。
场景：角色活动的地点，仅在场景中活动。
道具：如武器（提高攻击力）、衣服（提高防御）、消耗品（药剂、投掷物）、非消耗品（钥匙、信物）、特殊能力、技能等。

### 要素关系：
角色只能在场景中活动，可携带和使用道具。
场景可放置道具，角色可拾取道具。

### 全名机制：
角色格式：角色.称号.名字#全局ID(如：角色.将军.刘秀）。
场景格式：州.郡国.城镇.场景...（如：某州.某郡国.某城镇.某建筑）。
道具格式：道具.类型.名字（如：道具.武器.青龙偃月刀）。
注意!! 完整引用全名以确保一致性。
"""
