"""角色属性统计模型定义"""

from typing import final
from pydantic import BaseModel


###############################################################################################################################################
@final
class CharacterStats(BaseModel):
    """简化的角色属性统计，只包含核心战斗属性"""

    # 当前生命值
    hp: int = 0
    # 最大生命值
    max_hp: int = 10
    # 攻击力
    attack: int = 5
    # 防御力
    defense: int = 3
    # 每回合行动次数（能量）；决定该角色在 action_order 中出现几次
    energy: int = 1
    # 速度；决定 SPEED_ORDER 策略下的出手优先级，值越大越靠前
    speed: int = 0


###############################################################################################################################################
