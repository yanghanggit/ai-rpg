"""公共提示词模板模块

集中管理游戏中使用的通用提示词模板和构建函数。
包括场景环境描述模板和角色 kickoff 消息构建器。
"""

from typing import Final


# 场景启动提示词模板
PUBLIC_STAGE_KICK_OFF_MESSAGE: Final[
    str
] = """# 游戏启动! 以第三人称视角，直接描写场景内部的可见环境。
        
使用纯粹的感官描写：视觉、听觉、嗅觉、触觉等具体细节。
输出为单段紧凑文本，不使用换行或空行."""


def build_actor_kickoff_message(goal: str) -> str:
    """构建角色 kickoff 启动消息

    kickoff 的核心功能是让 Agent 进行自我定位和确认，而非进入具体场景。
    要求 Agent 以纯粹的心理描写确认身份、历史和目标，禁止场景化动作描写。

    Args:
        goal: 角色的具体目标描述

    Returns:
        格式化的 kickoff 启动消息
    """
    return f"""# 游戏启动！告诉我你是谁？请说出你的全名。并说出你的目标(回答简短)。

要求：
- 仅陈述身份、历史和目标
- 不要描写具体场景或动作（如"踏入训练场"、"目光扫过"等）
- 以第一人称心理独白的方式回答

你的目标是: {goal}"""
